#include "sudoku.h"

int main(int arc, char **argv)
{
    t_sudoku *sudoku = fill_sudoku_structure(arc, argv);
    if (solve(sudoku)) {
        show_sudoku(sudoku);
    } else {
        ft_putstr("non_valid sudoku\n");
    }
}



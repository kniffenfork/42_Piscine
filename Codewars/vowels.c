bool is_in_arr(char *s, char a){
    for (int i=0; s[i] != '\0'; i++){
        if (a == s[i])
            return true;
    }
    return false;
}

int get_count(char *s) {
    int res = 0;
    char vowels[] = {'a', 'e', 'i', 'o', 'u'};
    for (int i = 0; s[i] != '\0'; i++) {
        if (is_in_arr(vowels, s[i])){
            res++;
        }
    }
    return res;
}

